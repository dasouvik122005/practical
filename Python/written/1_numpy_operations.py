import numpy as np

# Create a NumPy array
arr = np.array([1, 2, 3, 4, 5])
print("Original array:", arr)

# Basic operations
# Addition
arr_add = arr + 5
print("Array after adding 5:", arr_add)

# Subtraction
arr_sub = arr - 2
print("Array after subtracting 2:", arr_sub)

# Multiplication
arr_mul = arr * 3
print("Array after multiplying by 3:", arr_mul)

# Division
arr_div = arr / 2
print("Array after dividing by 2:", arr_div)

# Squaring
arr_sq = arr ** 2
print("Array after squaring:", arr_sq)
